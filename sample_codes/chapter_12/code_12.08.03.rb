# バージョンはBundlerにおまかせ
gem 'faker'

# ----------------------------------------

# 1.7.2に固定
gem 'faker', '1.7.2'

# ----------------------------------------

# 1.7.2以上（上は制限なし）
gem 'faker', '>= 1.7.2'

# ----------------------------------------

# 1.7.2以上かつ1.8未満（1.7.11などは良いが、1.8.0はNG）
gem 'faker', '~> 1.7.2'

# ----------------------------------------

# 1.7以上かつ2.0未満（1.9.0などは良いが、2.0.0はNG）
gem 'faker', '~> 1.7'
